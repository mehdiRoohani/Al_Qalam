    public void getBankNames(JComboBox box) {
        try {
            box.removeAllItems();
            ps = cn.prepareStatement("SELECT bankName FROM bankList");
            rs = ps.executeQuery();
            while(rs.next()){
                box.addItem(rs.getString(1));
            }
        }
         catch(SQLException ex){
             JOptionPane.showMessageDialog(this, "Sql Exception");
         }
}
