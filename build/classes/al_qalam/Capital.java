    private void addOwnerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOwnerActionPerformed
        try{
            if(investment_field.getText().equals("") || investment_field.getText().equals("Investment") || Float.parseFloat(investment_field.getText())<1){
                JOptionPane.showMessageDialog(rootPane, "KINDLY DEFINE VALD VALUES");
            }
            else{
                switch(investmentMode){
                    case "Cash":
                        // <editor-fold defaultstate="collapsed" desc="Cash">
                        ps=cn.prepareStatement("SELECT TOP 1 balance FROM cashBook ORDER BY Id DESC");
                        rs=ps.executeQuery();
                        while(rs.next()){
                        balanceCashBook = rs.getFloat(1);
                        }
                        balanceCashBook=balanceCashBook+Float.parseFloat(investment_field.getText());
                        ps=cn.prepareStatement("INSERT INTO cashBook VALUES(GETDATE(),0,'"+ownerName_comboBox.getSelectedItem().toString()+"','Investment',"+investment_field.getText()+",'',"+balanceCashBook+")");
                        ps.execute();
    //                                    </editor-fold>
                        break;
                    case "Cheque":
                        // <editor-fold defaultstate="collapsed" desc="bankLedgerUpdate">
                        ps=cn.prepareStatement("SELECT bankId FROM bankList WHERE bankName='"+bankNameBox_owner.getSelectedItem().toString()+"'");
                        rs=ps.executeQuery();
                        while(rs.next()){
                            bankIdOwner=rs.getInt(1);
                        }
                        ps=cn.prepareStatement("SELECT TOP 1 balance,id FROM bankLedger WHERE bankId="+bankIdOwner+" ORDER BY id DESC");
                        rs=ps.executeQuery();
                        while(rs.next()){
                            bankBalanceOwner=rs.getFloat(1);
                        }
                        bankBalanceOwner=bankBalanceOwner+Float.parseFloat(investment_field.getText());
                        ps=cn.prepareStatement("INSERT INTO bankLedger VALUES("+bankIdOwner+",GETDATE(),0,'Investment',"+investment_field.getText()+",'',"+bankBalanceOwner+")");
                        ps.execute();
//            </editor-fold>
                        // <editor-fold defaultstate="collapsed" desc="Cheque">
                        ps=cn.prepareStatement("SELECT TOP 1 balance FROM chequeBook ORDER BY Id DESC");
                        rs=ps.executeQuery();
                        while(rs.next()){
                            balanceChequeBook=rs.getFloat(1);
                        }
                        balanceChequeBook=balanceChequeBook+Float.parseFloat(investment_field.getText());
                        ps=cn.prepareStatement("INSERT INTO chequeBook VALUES(GETDATE(),0,'"+bankNameBox_owner.getSelectedItem().toString()+"','"+ownerName_comboBox.getSelectedItem().toString()+"','Investment',"+investment_field.getText()+",'',"+balanceChequeBook+")");
                        ps.execute();
    //                                    </editor-fold>
                        break;
                }
                
                ps=cn.prepareStatement("UPDATE owners SET investment=investment+"+investment_field.getText()+"");
                ps.execute();
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, e.getLocalizedMessage());
        }
        investment_field.setForeground(Color.GRAY);
        investment_field.setText("Investment");
        ownerName_comboBox.requestFocus();
    }//GEN-LAST:event_addOwnerActionPerformed

