    public void focusGain(JTextField txt,String s){
        if(txt.getText().equals(s)){
            txt.setText(null);
            txt.setForeground(Color.black);
        }else{
            txt.selectAll();
        }
    }
    public void focusLost(JTextField txt,String s){
        if(txt.getText().isEmpty()){
            txt.setText(s);
            txt.setForeground(Color.GRAY);
        }
    }
